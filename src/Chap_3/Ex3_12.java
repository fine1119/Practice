package Chap_3;

public class Ex3_12 {
    public static void main(String[] args) {
        // 나머지 연산자(%) : 나머지값을 결과로 반환 (짝수, 홀수, 배수에 활용)

        int x = 10;
        int y = 8;

        System.out.printf("%d을 %d로 나누면, %n", x, y);
        System.out.printf("몫은 %d이고, 나머지는 %d입니다.%n", x / y, x % y);
    }
}
